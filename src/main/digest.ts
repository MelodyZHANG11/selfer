export interface CallProviderOpts {
  jsonMode?: boolean
  /** Per-call override of the provider and/or model selected from settings. */
  overrides?: {
    provider?: Settings['digestProvider']
    model?: string
  }
}

export async function callProvider(
  settings: Settings,
  system: string,
  userPrompt: string,
  opts: CallProviderOpts = {}
): Promise<string> {
  const provider = opts.overrides?.provider ?? settings.digestProvider
  const modelOverride = opts.overrides?.model
  switch (provider) {
    case 'anthropic-api':
      return callAnthropicApi(settings, system, userPrompt, modelOverride)
    case 'openai-compatible':
      return callOpenAICompatible(settings, system, userPrompt, opts, modelOverride)
    case 'claude-cli':
    default:
      return callClaudeCli(settings, system, userPrompt, modelOverride)
  }
}

async function callClaudeCli(
  settings: Settings,
  system: string,
  userPrompt: string,
  modelOverride?: string
): Promise<string> {
  const bin = resolveClaudeCli(settings.claudeCliPath)
  const args = ['-p', '--bare', '--append-system-prompt', system]
  const model = modelOverride || settings.claudeCliModel
  if (model) args.push('--model', model)

  return new Promise<string>((resolve, reject) => {
    const proc = spawn(bin, args, {
      stdio: ['pipe', 'pipe', 'pipe'],
      env: { ...process.env }
    })
    let stdout = ''
    let stderr = ''
    proc.stdout.on('data', (d: Buffer) => {
      stdout += d.toString('utf8')
    })
    proc.stderr.on('data', (d: Buffer) => {
      stderr += d.toString('utf8')
    })
    proc.on('error', reject)
    proc.on('close', (code) => {
      if (code === 0) resolve(stdout)
      else reject(new Error(`claude CLI exited ${code}: ${stderr || stdout || '(no output)'}`))
    })
    proc.stdin.write(userPrompt)
    proc.stdin.end()
  })
}

function resolveClaudeCli(override?: string): string {
  const candidates = [
    override,
    process.env.CLAUDE_CLI_PATH,
    path.join(os.homedir(), '.local/bin/claude'),
    '/opt/homebrew/bin/claude',
    '/usr/local/bin/claude',
    '/usr/bin/claude'
  ].filter((c): c is string => Boolean(c))
  for (const c of candidates) {
    if (fs.existsSync(c)) return c
  }
  throw new Error(
    "`claude` CLI not found. Install Claude Code (https://claude.com/code) or set its path in Settings."
  )
}

async function callAnthropicApi(
  settings: Settings,
  system: string,
  userPrompt: string,
  modelOverride?: string
): Promise<string> {
  if (!settings.anthropicApiKey)
    throw new Error('Anthropic API key not set — configure it in Settings.')
  const client = new Anthropic({ apiKey: settings.anthropicApiKey })
  const msg = await client.messages.create({
    model: modelOverride || settings.anthropicModel || 'claude-opus-4-7',
    max_tokens: 16000,
    system,
    messages: [{ role: 'user', content: userPrompt }]
  })
  return msg.content
    .filter((b): b is Anthropic.TextBlock => b.type === 'text')
    .map((b) => b.text)
    .join('\n\n')
}

const TRANSIENT_STATUSES = new Set([429, 502, 503, 504])
const MAX_ATTEMPTS = 3

async function callOpenAICompatible(
  settings: Settings,
  system: string,
  userPrompt: string,
  opts: CallProviderOpts,
  modelOverride?: string
): Promise<string> {
  const base = (settings.openaiBaseUrl || '').replace(/\/$/, '')
  if (!base) throw new Error('OpenAI-compatible base URL not set — configure it in Settings.')
  const model = modelOverride || settings.openaiModel || 'gpt-4o-mini'
  const headers: Record<string, string> = { 'Content-Type': 'application/json' }
  if (settings.openaiApiKey) headers.Authorization = `Bearer ${settings.openaiApiKey}`

  const messages = [
    { role: 'system', content: system },
    { role: 'user', content: userPrompt }
  ]
  const maxOut = 16000
  const url = `${base}/chat/completions`
  const baseBody: Record<string, unknown> = { model, messages, max_completion_tokens: maxOut }
  if (opts.jsonMode) baseBody.response_format = { type: 'json_object' }

  let res: Response | null = null
  let lastTransient: { status: number; statusText: string } | null = null
  for (let attempt = 1; attempt <= MAX_ATTEMPTS; attempt++) {
    try {
      res = await fetch(url, {
        method: 'POST',
        headers,
        body: JSON.stringify(baseBody)
      })
    } catch (err) {
      if (attempt === MAX_ATTEMPTS) {
        throw new Error(
          `Could not reach provider at ${base}. Check your network and base URL. (${(err as Error).message})`
        )
      }
      console.warn(`[provider] network error (attempt ${attempt}/${MAX_ATTEMPTS}): ${(err as Error).message} — retrying`)
      await sleep(backoffMs(attempt))
      continue
    }

    if (res.ok || !TRANSIENT_STATUSES.has(res.status)) break

    lastTransient = { status: res.status, statusText: res.statusText }
    if (attempt === MAX_ATTEMPTS) break
    console.warn(`[provider] ${res.status} ${res.statusText} (attempt ${attempt}/${MAX_ATTEMPTS}) — retrying`)
    await sleep(backoffMs(attempt))
  }

  if (!res) {
    // Unreachable — the loop either assigns res or throws — but satisfies the type checker.
    throw new Error('Provider call failed before any response.')
  }

  if (!res.ok && res.status === 400) {
    const errText = await res.text().catch(() => '')
    if (shouldFallbackToMaxTokens(errText)) {
      const fallbackBody: Record<string, unknown> = { model, messages, max_tokens: maxOut }
      if (opts.jsonMode) fallbackBody.response_format = { type: 'json_object' }
      res = await fetch(url, {
        method: 'POST',
        headers,
        body: JSON.stringify(fallbackBody)
      })
    } else {
      throw new Error(`OpenAI-compatible call failed: ${res.status} ${res.statusText}\n${errText}`)
    }
  }

  if (!res.ok) {
    if (lastTransient && TRANSIENT_STATUSES.has(res.status)) {
      throw new Error(
        `Provider temporarily unavailable (${res.status} ${res.statusText}). Tried ${MAX_ATTEMPTS} times. Please try again in a moment.`
      )
    }
    const body = await res.text().catch(() => '')
    throw new Error(`OpenAI-compatible call failed: ${res.status} ${res.statusText}\n${body}`)
  }

  const data = (await res.json()) as {
    choices?: { message?: { content?: string } }[]
  }
  const content = data.choices?.[0]?.message?.content
  if (!content) throw new Error('OpenAI-compatible response had no content.')
  return content
}

function backoffMs(attempt: number): number {
  const base = 1000 * Math.pow(2, attempt - 1)
  return base + Math.floor(Math.random() * 250)
}

function sleep(ms: number): Promise<void> {
  return new Promise((resolve) => setTimeout(resolve, ms))
}

function shouldFallbackToMaxTokens(errText: string): boolean {
  const t = errText.toLowerCase()
  return (
    (t.includes('max_completion_tokens') &&
      (t.includes('unknown') ||
        t.includes('unsupported') ||
        t.includes('not supported') ||
        t.includes('unrecognized') ||
        t.includes('not allowed'))) ||
    (t.includes('use') && t.includes('max_tokens'))
  )
}

