export function listDigests(db: DB): DigestInfo[] {
  const rows = db
    .prepare('SELECT date, path, generated_at as generatedAt FROM digests ORDER BY date DESC')
    .all() as DigestInfo[]
  return rows.filter((r) => fs.existsSync(r.path))
}

export async function readDigest(filePath: string): Promise<string> {
  return fsp.readFile(filePath, 'utf8')
}

/**
 * Returns a structured DigestDoc for a date — uses DB tables when present,
 * otherwise parses the on-disk markdown into sections with no references.
 */
export async function getDigestDoc(db: DB, date: string): Promise<DigestDoc> {
  const info = db
    .prepare('SELECT date, path, generated_at as generatedAt FROM digests WHERE date = ?')
    .get(date) as DigestInfo | undefined
  if (!info) throw new Error(`No digest for ${date}`)

  if (hasStructuredDigest(db, date)) {
    return readDigestDoc(db, info)
  }

  // Legacy fallback: parse the .md by ## headings.
  const md = await fsp.readFile(info.path, 'utf8').catch(() => '')
  const sections = parseLegacyMarkdown(md)
  return {
    info,
    sections: sections.map((s, i) => ({
      sectionId: s.sectionId || `section-${i + 1}`,
      heading: s.heading,
      kind: s.kind,
      bodyMd: s.bodyMd,
      items: s.items.map((it) => ({
        itemId: it.itemId,
        textMd: it.textMd,
        refs: [],
        refinedAt: null
      })),
      refs: [],
      refinedAt: null,
      hasHistory: false
    })),
    structured: false
  }
}

/**
 * Parse a markdown digest body into rough sections + bullets.
 * Best-effort — used only for digests generated before structured persistence existed.
 */
export function parseLegacyMarkdown(md: string): ParsedSection[] {
  if (!md) return []
  // Strip the leading H1 ("# Work Diary — …") and the trailing "## Sessions covered" footer.
  const lines = md.split(/\r?\n/)
  const sectionStarts: number[] = []
  for (let i = 0; i < lines.length; i++) {
    if (/^##\s+/.test(lines[i])) sectionStarts.push(i)
  }
  if (sectionStarts.length === 0) return []

  const out: ParsedSection[] = []
  for (let s = 0; s < sectionStarts.length; s++) {
    const start = sectionStarts[s]
    const end = s + 1 < sectionStarts.length ? sectionStarts[s + 1] : lines.length
    const headingLine = lines[start].replace(/^##\s+/, '').trim()
    if (/^sessions covered/i.test(headingLine)) continue // drop footer

    const bodyLines = lines.slice(start + 1, end).join('\n').trim()
    const sectionId = slugify(headingLine) || `section-${out.length + 1}`

    // Detect kind by counting list markers.
    const bulletLines = bodyLines.split(/\r?\n/).filter((l) => /^\s*[-*]\s+/.test(l))
    const nonBulletText = bodyLines
      .split(/\r?\n/)
      .filter((l) => l.trim() && !/^\s*[-*]\s+/.test(l))
      .join('\n')
      .trim()

    let kind: 'bullets' | 'paragraph' | 'mixed' = 'paragraph'
    if (bulletLines.length > 0 && !nonBulletText) kind = 'bullets'
    else if (bulletLines.length > 0) kind = 'mixed'

    const items: ParsedItem[] = bulletLines.map((l, i) => ({
      itemId: `i${i + 1}`,
      textMd: l.replace(/^\s*[-*]\s+/, '').trim(),
      refIds: []
    }))

    out.push({
      sectionId,
      ord: out.length,
      heading: headingLine,
      kind,
      bodyMd: bodyLines,
      items,
      refIds: []
    })
  }
  return out
}

/**
 * Used by digestRefine to update the on-disk .md after a section/item refine.
 */
export async function rewriteDigestFile(db: DB, date: string): Promise<void> {
  const info = db
    .prepare('SELECT date, path, generated_at as generatedAt FROM digests WHERE date = ?')
    .get(date) as DigestInfo | undefined
  if (!info) return
  const window = localDayWindow(date)
  const sessions = findSessionsInWindow(db, window.startIso, window.endIso)
  if (!hasStructuredDigest(db, date)) return // nothing to render
  const doc = readDigestDoc(db, info)
  const md = renderDocMarkdown(doc, sessions)
  await fsp.writeFile(info.path, md, 'utf8')
  const generatedAt = new Date().toISOString()
  db.prepare('UPDATE digests SET generated_at = ? WHERE date = ?').run(generatedAt, date)
}

/**
 * The day's full timeline, used by the refine UI's "add reference" picker.
 */
export async function listDigestTimeline(db: DB, date: string): Promise<DigestRef[]> {
  const window = localDayWindow(date)
  const sessions = findSessionsInWindow(db, window.startIso, window.endIso)
  return buildReferences(db, sessions)
}

